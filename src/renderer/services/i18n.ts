export type Language = 'pt-BR' | 'en-US'
export type TranslationKeys = typeof translations['pt-BR']

export function getTranslation(lang: Language): TranslationKeys {
  return translations[lang] || translations['pt-BR']
}

export function t(lang: Language, path: string): string {
  const keys = path.split('.')
  let value: any = translations[lang] || translations['pt-BR']
  
  for (const key of keys) {
    if (value && typeof value === 'object' && key in value) {
      value = value[key]
    } else {
      return path
    }
  }
  
  return typeof value === 'string' ? value : path
}
